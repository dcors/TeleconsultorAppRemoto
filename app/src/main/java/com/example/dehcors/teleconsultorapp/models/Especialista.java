public class Especialista {
    private String especialistaCPF;
    private String especialistaNome;
    private String especialistaProfissao;
    private String especialistaSenha;
    private String especialistaRepitaSenha;

    public String getEspecialistaCPF() {
        return especialistaCPF;
    }

    public void setEspecialistaCPF(String especialistaCPF) {
        this.especialistaCPF = especialistaCPF;
    }

    public String getEspecialistaNome() {
        return especialistaNome;
    }

    public void setEspecialistaNome(String especialistaNome) {
        this.especialistaNome = especialistaNome;
    }

    public String getEspecialistaProfissao() {
        return especialistaProfissao;
    }

    public void setEspecialistaProfissao(String especialistaProfissao) {
        this.especialistaProfissao = especialistaProfissao;
    }

    public String getEspecialistaSennha() {
        return especialistaSenha;
    }

    public void setEspecialistaSenha(String especialistaSenha) {
        this.especialistaSenha = especialistaSenha;
    }

    public String getEspecialistaRepitaSenha() {
        return especialistaRepitaSenha;
    }

    public void setEspecialistaRepitaSenha(String especialistaRepitaSenha) {
        this.especialistaRepitaSenha = especialistaRepitaSenha;
    }
}
